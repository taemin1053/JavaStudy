package method;

//두 숫자를 입력 받아서 더하고 출력하는 기능 개발
public class Method1 {
    public static void main(String[] args) {

        int a = 1;
        int b = 2;
        System.out.print(a+ "+" + b + "연산수행");
        int sum = a+b;
        System.out.println("결과출력1: "+sum);

        int x = 10;
        int y = 20;
        System.out.println(x + "+" + y+"연산 수행");
        int sum2 = x+y;
        System.out.println("결과 출력2:"+sum2);
    }
}
